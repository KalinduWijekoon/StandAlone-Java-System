    private void jPanel10MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel10MouseClicked
        // TODO add your handling code here;
        DecimalFormat df = new DecimalFormat("#.00");
        String sumofsales=null;
        String sumofpaitent = null;
        String sumofcheque = null;
        try{
            Connection c = DB.mycon();
            Statement s = c.createStatement();
            Statement s1 = c.createStatement();
            Statement s2 = c.createStatement();
            ResultSet rs = s.executeQuery("SELECT SUM(total_price) FROM sales");
            ResultSet rs1 = s1.executeQuery("SELECT SUM(DoctorFee)+SUM(ChanelFee) FROM appointment");
            ResultSet rs2 = s2.executeQuery("SELECT SUM(income) FROM finance_cheq");
            if(rs.next()){
                if(rs.getString("SUM(total_price)")!=null){
                sumofsales = rs.getString("SUM(total_price)"); 
                }else{
                    sumofsales = "0";
                }
               
            }
            if(rs1.next()){
                if(rs1.getString("SUM(DoctorFee)+SUM(ChanelFee)")!=null){
                sumofpaitent = rs1.getString("SUM(DoctorFee)+SUM(ChanelFee)");
                }else{
                    sumofpaitent ="0";
                }
            }
            if(rs2.next()){
                if(rs2.getString("SUM(income)")!=null){
                sumofcheque = rs2.getString("SUM(income)");
                }else{
                   sumofcheque = "0"; 
                }
            }
            
            jLabel3.setText(df.format(Double.parseDouble(sumofsales)+Double.parseDouble(sumofpaitent)+Double.parseDouble(sumofcheque)));
        }catch(Exception e){
            e.printStackTrace();
        }
        jLabel15.setText(df.format(Double.parseDouble(sumofsales)));
        jLabel14.setText(df.format(Double.parseDouble(sumofpaitent)));
        jLabel9.setText(df.format(Double.parseDouble(sumofcheque)));
    }//GEN-LAST:event_jPanel10MouseClicked

